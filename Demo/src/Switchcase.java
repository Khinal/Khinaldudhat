
public class Switchcase {

	public static void main(String[] args) {
		
		int choice = 1;
		
		switch (choice)
		{
		case 1 :
			System.out.println("January");
		
			break;
			
			
		case 2 :
			System.out.println("February");
			
			break;
			
		 default :
			System.out.println("Invalid Case");
			
		}
			
		
	 }
	}

